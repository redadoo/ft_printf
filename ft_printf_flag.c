#include"ft_printf.h"

void	flag(unsigned char c ,char *str)
{
	char l;
    str = 0;
	if(c == ' ')
	{
		l = ' ';
		write(1, &l, 1);
	}
	if(c == '+')
	{
		l = '+';
		write(1, &l, 1);
	}
}